namespace MRef.Demo.Enumeration
{
    // Enumeration
    /// <summary>
    /// Enumeration ColorType
    /// </summary>
    /// <remarks>
    /// <para>
    /// Red/Blue/Yellow can become all color you want.
    /// </para>
    /// <list type="bullet">
    /// <listItem>
    /// Orange = Red + Yellow
    /// </listItem>
    /// <listItem>
    /// Purple = Red + Blue
    /// </listItem>
    /// <listItem>
    /// Green = Blue + Yellow
    /// </listItem>
    /// </list>
    /// </remarks>
    /// <seealso cref="T:System.Object"/>
    public enum ColorType
    {
        /// <summary>
        /// this color is red
        /// </summary>
        Red = 0,
        /// <summary>
        /// blue like river
        /// </summary>
        Blue = 1,
        /// <summary>
        /// yellow comes from desert
        /// </summary>
        Yellow = 2,
        /// <summary>
        /// purple rain, purple rain
        /// </summary>
        Purple = 4
    }
}
